function getAbility(id, slot) {
  const entry = POKEMON_ABILITIES[id]
  if (!entry || !entry[slot]) return null
  const key = entry[slot]
  return ABILITY_DATA[key] ? { key, ...ABILITY_DATA[key] } : null
}
function getPokemonAbility(id) {
  return getAbility(id, 0)
}
function getEffectiveness(atkType, defTypes) {
  let mult = 1
  for (const dt of defTypes) {
    const chart = TYPE_CHART[atkType]
    if (chart && chart[dt] !== undefined) mult *= chart[dt]
  }
  return mult
}
